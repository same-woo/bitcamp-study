package bitcamp.ex05;

//# 산술 연산자 : +, -, *, /, %
// 정수와 정수간의 연산 결과는 항상 정수값이 나온다 !
public class Exam0110 {
  public static void main(String[] args) {
    System.out.println(100 + 27);
    System.out.println(100 - 27);
    System.out.println(100 * 27);
    System.out.println(100 / 27);
    System.out.println(100 % 27); // 나눈 나머지 
  }
}